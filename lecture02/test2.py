#เฃื่อมข้อมูล
# ","
score = 500
print("Hello...", 200, True, 2993.22, 10/2, "Bruv", score)
# "+"
print("Hello..." + str(200) + str(True) + str(2993.22) + str(10/2) + "Bruv" + str(score))
# .format
print("Hello... {} {} {} {} Bruv {}".format(200, True, 2993.22, 10/2, score))
# f-string
print(f"Hello... {200} {True} {2993.22} {10/2} Bruv {score}")
# modular operator (%)
print("Hello... %d %f %s Bruv" % (20, 17.5, "SAU"))
